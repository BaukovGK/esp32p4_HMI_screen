/**
 * @file display_init.h
 * @brief Заголовочный файл модуля инициализации дисплея.
 *
 * Объявляет функцию display_init(), которая выполняет:
 * - Предварительную инициализацию MCU дисплея Waveshare (I2C 0x45)
 * - Настройку ШИМ-подсветки через LEDC
 * - Включение питания PHY через внутренний LDO
 * - Создание шины MIPI DSI и DBI-интерфейса для команд
 * - Инициализацию панели JD9365 (800x1280, RGB565)
 * - Регистрацию дисплея в LVGL с программным поворотом на 270 градусов (ландшафт 1280x800)
 *
 * Требует: board_i2c_init() должен быть вызван до display_init().
 */
#pragma once

#include "esp_err.h"
#include "lvgl.h"

/**
 * Инициализация дисплея MIPI DSI с контроллером JD9365 и регистрация в LVGL.
 *
 * @return Указатель на LVGL-дисплей (lv_display_t*), или NULL при ошибке
 */
lv_display_t *display_init(void);

/**
 * Деинициализация дисплейной подсистемы.
 *
 * Освобождает все ресурсы, захваченные display_init(): канал LDO MIPI DSI PHY,
 * шину DSI, panel I/O, panel handle и LVGL port. Подсветка (LEDC канал)
 * не освобождается — она привязана к отдельному GPIO и безопасна в любом состоянии.
 *
 * Безопасно вызывать повторно и сразу после неудачного display_init().
 */
void display_deinit(void);

/**
 * Установка яркости подсветки дисплея (0-100%).
 *
 * Может быть вызвана в любой момент после display_init(). Подсветка стартует
 * выключенной (=0) после инициализации панели. Включение подсветки должно
 * произойти ПОСЛЕ первого lv_refr_now(disp), иначе пользователь видит мусор
 * RAM панели между инициализацией и первым flush-ем LVGL. Вызови эту функцию
 * из app_main.c сразу после ui_init().
 *
 * @param brightness_percent Яркость: 0 (выключено) до 100 (полная)
 * @return ESP_OK на успех, иначе ошибка LEDC
 */
esp_err_t backlight_set(int brightness_percent);
