/**
 * @file scr_settings.h
 * @brief Экран "Настройки" -- редактирование уставок через вкладки и цифровую клавиатуру.
 *
 * Левая часть (800 px) -- TabView с 4 вкладками:
 *   1. Давление (P1/P3/P4 max, dP предупреждение)
 *   2. Дозатор (время работы, время цикла)
 *   3. Промывка (целевая/макс. температура, гистерезис, таймауты)
 *   4. Таймауты (подтверждение насоса, разгон)
 * Правая часть (420 px) -- экранная цифровая клавиатура (numpad).
 * При нажатии "Применить" значения отправляются по MQTT.
 */
#pragma once

#include "lvgl.h"
#include "plant_data.h"

/**
 * Создаёт экран настроек (вкладки + numpad).
 * Возвращает корневой контейнер с user_data = settings_widgets_t*.
 */
lv_obj_t *scr_settings_create(lv_obj_t *parent);

/**
 * Загружает начальные значения уставок в пустые текстовые поля.
 * Не перезаписывает поля, уже отредактированные пользователем.
 * @param container  корневой контейнер
 * @param data       актуальные данные установки (кэш уставок)
 */
void      scr_settings_update(lv_obj_t *container, const plant_data_t *data, uint32_t dirty);
