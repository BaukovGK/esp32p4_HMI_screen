/**
 * @file ui_main.h
 * @brief Главный модуль пользовательского интерфейса установки обратного осмоса.
 *
 * Определяет идентификаторы экранов и функции инициализации/переключения UI.
 * Управляет жизненным циклом экранов: создание, обновление, удаление.
 */
#pragma once

#include "lvgl.h"

/**
 * Идентификаторы экранов UI.
 * Каждый экран соответствует отдельной функциональной области HMI.
 */
typedef enum {
    SCREEN_MNEMONIC = 0,   // Мнемосхема технологического процесса
    SCREEN_PARAMETERS,     // Параметры датчиков (давление, расход, проводимость)
    SCREEN_WASHING,        // Управление химической промывкой мембран
    SCREEN_ALARMS,         // Журнал аварий и предупреждений
    SCREEN_SETTINGS,       // Настройки уставок и параметров
    SCREEN_DIAGNOSTICS,    // Диагностика системы (память, аптайм, Modbus)
    SCREEN_COUNT,          // Общее количество экранов
} screen_id_t;

/**
 * Инициализация пользовательского интерфейса.
 * Создаёт тему, панель аварий, область контента, навигационную панель
 * и запускает таймер периодического обновления данных (250 мс).
 *
 * @param disp указатель на дисплей LVGL (не используется напрямую)
 */
void ui_init(lv_display_t *disp);

/**
 * Переключение на указанный экран.
 * Удаляет текущий экран, создаёт новый и обновляет навигационную панель.
 *
 * @param id идентификатор целевого экрана
 */
void ui_switch_screen(screen_id_t id);
