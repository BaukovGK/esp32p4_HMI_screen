/**
 * @file lang.h
 * @brief Модуль интернационализации (i18n) -- публичный интерфейс.
 *
 * Архитектура:
 *   - Каждый язык хранится в виде массива строк const char*[], индексированного
 *     значениями перечисления str_id_t (определено в lang_strings.h).
 *   - Переключение языка происходит заменой указателя на текущую таблицу строк.
 *   - Для получения локализованной строки в UI используется функция lang_str(id).
 *
 * Добавление нового языка:
 *   1. Добавить значение в перечисление lang_id_t (например, LANG_DE).
 *   2. Создать файл lang_de.c с массивом lang_de_strings[STR_COUNT].
 *   3. Объявить extern-массив в этом заголовочном файле.
 *   4. Зарегистрировать массив в lang_init() (файл lang.c).
 */
#pragma once

#include "lang_strings.h"

/**
 * @brief Идентификаторы поддерживаемых языков.
 *
 * LANG_COUNT используется как размер внутреннего массива таблиц строк.
 */
typedef enum {
    LANG_EN = 0,  // Английский
    LANG_RU,      // Русский
    LANG_COUNT,   // Общее количество языков (служебное значение)
} lang_id_t;

/**
 * @brief Инициализация модуля i18n.
 *
 * Заполняет внутренний массив таблиц строк указателями на lang_en_strings
 * и lang_ru_strings. Загружает язык из NVS если доступен, иначе использует
 * fallback_lang. Вызывается один раз при старте приложения (app_main).
 *
 * @param fallback_lang Язык, используемый если в NVS нет сохранённого языка.
 * @return Фактически выбранный язык (из NVS или fallback_lang).
 */
lang_id_t   lang_init(lang_id_t fallback_lang);

/**
 * @brief Смена текущего языка интерфейса.
 *
 * После вызова все последующие обращения к lang_str() будут возвращать
 * строки на выбранном языке. UI-виджеты необходимо обновить вручную.
 *
 * @param lang Идентификатор языка (LANG_EN или LANG_RU).
 */
void        lang_set(lang_id_t lang);

/**
 * @brief Получить идентификатор текущего активного языка.
 * @return Текущий язык (lang_id_t).
 */
lang_id_t   lang_get(void);

/**
 * @brief Получить локализованную строку по идентификатору.
 *
 * Основная функция, используемая во всех UI-экранах.
 * При некорректном id или отсутствующей строке возвращает "???".
 *
 * @param id Строковый идентификатор из перечисления str_id_t.
 * @return Указатель на UTF-8 строку на текущем языке.
 */
const char *lang_str(str_id_t id);

/* Таблицы строк для каждого языка (определены в lang_en.c и lang_ru.c) */
extern const char *lang_en_strings[];
extern const char *lang_ru_strings[];
